public class BinaryOperators {
    // Modulo

    public float promotedToFloatModulo(float float1) {
	return float1 % 2.0f;
    }
    public double promotedToDoubleModulo(float float1) {
	return float1 % 2.0d;
    }
    public double promotedToDoubleModulo(double double1) {
	return double1 % 2.0f;
    }

}